% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
global estado;
global s;
global i;
global p;
global val;
global h;
global m;
global ss;
global memoria;
global aux;
estado=1;
s = Bluetooth('SEGUILINEA', 1);
%disp=instrfind;
%s=serial('COM7');%disp(1); %creación del puerto
%s.BaudRate=115200; % configuración del puerto
%s.Timeout=4;
%s.InputBufferSize=16; %tamaño de el buffer de entrada
fopen(s);%abrir el puerto
while (estado==1)
% while(~(s.BytesAvailable))
% end
%q=fgetl(s) %lectura del puerto
q=fscanf(s);
l=size(q);
flushinput(s);
if(l(2)>4 && isempty(q)==0)
    ind=str2double(q(1:3));
    if(ind~=memoria)
        aux=(str2double(q(3:end)));%/100.0;
        if(aux>0)
            i=i+1;
            t1 = datetime('now','Format','HH:mm:ss');
            [hh,mm,sss] = hms(t1);
            h(i)=hh;
            m(i)=mm;
            ss(i)=sss;
            p(i)=i;
            val(i)=aux;
            xlswrite('6MV.xls', [h' m' ss' p' val']);
            set(handles.edit1,'String',num2str(min(val)));
            set(handles.edit2,'String',num2str(max(val)));
        end
        memoria=ind;
    end
end
if(aux==0)
    set(handles.text4,'String','Iniciando...');
elseif(isempty(q)==1)
    set(handles.text4,'String','Desconexion');
else
        set(handles.text4,'String','Normal');
        plot(p,val);
        title('Genetic Algorithm')
        xlabel('Iteration')
        ylabel('Fitness')        
end
drawnow();
pause(0.2);
end 


